// 根据菜品名称智能选择最可能有的平台
function getBestPlatforms(foodName) {
    const platforms = [];
    
    // 检测菜品名称特征来选择平台
    const foodNameLower = foodName.toLowerCase();
    
    // 1. 首先总是尝试百度百科
    platforms.push('baidu');
    
    // 2. 如果是中餐，优先尝试食谱平台
    if (foodNameLower.includes('宫保') || 
        foodNameLower.includes('红烧') || 
        foodNameLower.includes('清蒸') ||
        foodNameLower.includes('鱼香') ||
        foodNameLower.includes('麻婆') ||
        foodNameLower.includes('烤') ||
        foodNameLower.includes('炒') ||
        foodNameLower.includes('炖') ||
        foodNameLower.includes('蒸')) {
        platforms.push('douguo');
        platforms.push('xiachufang');
    }
    
    // 3. 如果是西餐或日料，尝试维基百科
    if (foodNameLower.includes('披萨') || 
        foodNameLower.includes('意面') || 
        foodNameLower.includes('寿司') ||
        foodNameLower.includes('拉面') ||
        foodNameLower.includes('牛排') ||
        foodNameLower.includes('汉堡') ||
        foodNameLower.includes('沙拉') ||
        foodNameLower.includes('三明治')) {
        platforms.push('wikipedia');
    }
    
    // 4. 添加美食天下作为备选
    if (!platforms.includes('douguo') && !platforms.includes('xiachufang')) {
        platforms.push('meishitianxia');
    }
    
    // 去重并过滤不可用的平台
    return [...new Set(platforms)].filter(platform => PLATFORM_AVAILABILITY[platform]);
}

// 生成平台链接
function generatePlatformLink(foodName, platformKey) {
    const platform = PLATFORM_CONFIG[platformKey];
    if (!platform) return null;
    
    const encodedFoodName = encodeURIComponent(foodName);
    let url = platform.baseUrl + encodedFoodName;
    
    if (platform.searchParam) {
        url += platform.searchParam;
    }
    
    return {
        url: url,
        name: platform.name,
        icon: platform.icon,
        colorClass: platform.colorClass
    };
}

// 为菜品生成多平台链接HTML
function generatePlatformLinksHTML(foodName, containerClass = 'platform-links') {
    const bestPlatforms = getBestPlatforms(foodName);
    if (bestPlatforms.length === 0) return '';
    
    let html = `<div class="${containerClass}">`;
    
    bestPlatforms.forEach(platformKey => {
        const linkInfo = generatePlatformLink(foodName, platformKey);
        if (linkInfo) {
            html += `
                <a href="${linkInfo.url}" 
                   target="_blank" 
                   class="platform-link ${linkInfo.colorClass}" 
                   title="在${linkInfo.name}搜索${foodName}">
                    ${linkInfo.icon} ${linkInfo.name}
                </a>
            `;
        }
    });
    
    html += '</div>';
    
    // 添加智能提示（仅在推荐结果区域显示）
    if (containerClass === 'result-platform-links' && bestPlatforms.length > 1) {
        const platformNames = bestPlatforms.map(p => PLATFORM_CONFIG[p].name).join('、');
        html += `<div class="platform-hint">智能推荐平台：${platformNames}</div>`;
    }
    
    return html;
}

