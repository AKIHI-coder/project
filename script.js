// 快速筛选
function toggleQuickFilter(tag) {
    if (tag === 'all') {
        clearAllFilters();
        return;
    }

    // 移除同类型的快速筛选
    const filterTypes = ['鸡肉', '猪肉', '牛肉', '辣', '甜', '蒸', '炸'];
    activeFilters = activeFilters.filter(filter => !filterTypes.includes(filter.value));
    
    // 添加新的筛选
    activeFilters.push({
        level: 'ingredient',
        value: tag,
        displayLevel: '食材'
    });
    
    updateActiveFiltersDisplay();
    updateStats();
}

// 添加筛选条件
function addFilter() {
    const level = filterLevelSelect.value;
    const value = filterValueInput.value.trim();
    
    if (!value) {
        alert('请输入筛选条件！');
        return;
    }
    
    // 检查是否已存在相同条件的筛选
    const exists = activeFilters.some(filter => 
        filter.level === level && filter.value === value
    );
    
    if (exists) {
        alert('该筛选条件已存在！');
        return;
    }
    
    // 添加新筛选条件
    activeFilters.push({
        level: level,
        value: value,
        displayLevel: filterTypeMap[level]
    });
    
    // 清空输入框
    filterValueInput.value = '';
    
    // 更新UI
    updateActiveFiltersDisplay();
    updateStats();
}

// 移除筛选条件
function removeFilter(index) {
    activeFilters.splice(index, 1);
    updateActiveFiltersDisplay();
    updateStats();
}

// 更新当前筛选条件显示
function updateActiveFiltersDisplay() {
    activeFiltersContainer.innerHTML = '';
    
    activeFilters.forEach((filter, index) => {
        const filterTag = document.createElement('div');
        filterTag.className = 'filter-tag';
        filterTag.innerHTML = `
            <span class="level">${filter.displayLevel}:</span>
            <span class="value">${filter.value}</span>
            <button class="remove-filter" onclick="removeFilter(${index})">×</button>
        `;
        activeFiltersContainer.appendChild(filterTag);
    });
}

// 清空所有筛选条件
